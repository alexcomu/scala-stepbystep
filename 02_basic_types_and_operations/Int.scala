// Integer Literals

// Hecadecimal
val hex = 0x5
val hex2 = 0x00FF
val hex3 = 0xcafebabe
println(hex, hex2, hex3)

// Decimal
val dec = 5
val dec2 = 10
val dec3 = 10000
println(dec, dec2, dec3)

// Long
val long_hex = 0x5L
val long_dec = 5L
println(long_hex, long_dec)

// Short or Byte
val byte_dec: Byte = 5
val short_dec: Short = 5
println(byte_dec, short_dec)
