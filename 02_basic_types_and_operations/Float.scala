// Floating point literals
// All this numbers are Double!
val big = 1.2345
val bigger = 1.2345e1
val biggerStill = 1.2345E45
val otherDouble = 3e5
val anotherDouble = 3e5D
// All this numbers are Float!
val little = 1.2345F
val littleBigger = 3e5F

println(big, bigger, biggerStill, otherDouble, anotherDouble)
println(little, littleBigger)
